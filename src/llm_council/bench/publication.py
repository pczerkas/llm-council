"""Bench results publication (ADR-048 P3, #420).

Renders the docs-site results page FROM the harness output — never
hand-edited, so published numbers cannot drift from the run that produced
them. Reproducibility first: dataset version, run date, costs, and a
methodology pointer are mandatory fields.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any, Dict, List, Optional

from .harness import BenchRun
from .matrix import format_matrix_table


def _cell(text: str) -> str:
    """Escape markdown-table pipes in cell content (#441 review)."""
    return str(text).replace("|", "\\|")


def render_results_page(
    run: BenchRun,
    *,
    dataset_version: str,
    matrix_rows: Optional[List[Dict[str, Any]]] = None,
) -> str:
    """Render the full results page as markdown."""
    date = run.started_at.split("T")[0]
    cost = (
        f"${run.total_cost_usd:.4f}"
        if run.cost_known
        else f"~${run.total_cost_usd:.4f} (cost not fully known)"
    )
    lines = [
        "# Bench Results",
        "",
        "> Regenerated by `llm-council bench` — do not edit by hand "
        "(ADR-048 P3; numbers must match the run that produced them).",
        "",
        f"- **Dataset:** `bench/dataset/{dataset_version}`",
        f"- **Run date:** {date}",
        f"- **Spend:** {cost}",
        f"- **Within envelope:** {run.items_passed}/{run.items_run}"
        + (f" — ABORTED: {run.aborted}" if run.aborted else ""),
        "- **Methodology & caveats:** see `bench/METHODOLOGY.md` — small-N, "
        "judge-bias inheritance (ADR-047), fixed-config comparability.",
        "",
        "## Per-item results",
        "",
        "| item | domain | result | score | cost (USD) |",
        "|------|--------|--------|-------|------------|",
    ]
    for r in run.results:
        status = "pass" if r.ok else f"FAIL ({_cell('; '.join(r.failures))})"
        score = f"{r.score:.2f}" if r.score is not None else "n/a"
        item_cost = f"{r.cost_usd:.4f}" if r.cost_known else "unknown"
        lines.append(
            f"| {_cell(r.item_id)} | {_cell(r.domain)} | {status} | {score} | {item_cost} |"
        )
    if matrix_rows:
        lines.append("")
        lines.append(format_matrix_table(matrix_rows))
    lines.append("")
    return "\n".join(lines)


def write_results_page(
    run: BenchRun,
    path: Path,
    *,
    dataset_version: str,
    matrix_rows: Optional[List[Dict[str, Any]]] = None,
) -> Path:
    """Regenerate the page at ``path`` (idempotent for identical runs)."""
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(
        render_results_page(
            run, dataset_version=dataset_version, matrix_rows=matrix_rows
        ),
        encoding="utf-8",
    )
    return path
